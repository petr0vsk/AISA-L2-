import streamlit as st

from config import get_openrouter_api_key, DEFAULT_SYSTEM_PROMPT
from llm_client import create_chat_runnable
from chat_ui import (
    init_chat_history,
    reset_chat_history,
    render_history,
    add_user_message,
    add_assistant_message,
    init_session_id,
    reset_session_id,
)


def main() -> None:
    """
    –¢–æ—á–∫–∞ –≤—Ö–æ–¥–∞ –≤ Streamlit-–ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ.

    –ó–¥–µ—Å—å:
    * –Ω–∞—Å—Ç—Ä–∞–∏–≤–∞–µ—Ç—Å—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞;
    * —Ä–µ–Ω–¥–µ—Ä–∏—Ç—Å—è —Å–∞–π–¥–±–∞—Ä —Å –Ω–∞—Å—Ç—Ä–æ–π–∫–∞–º–∏;
    * –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç—Å—è –∏ –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏ –æ—á–∏—â–∞–µ—Ç—Å—è –∏—Å—Ç–æ—Ä–∏—è —á–∞—Ç–∞;
    * –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç—Å—è/–æ–±–Ω–æ–≤–ª—è–µ—Ç—Å—è session_id –¥–ª—è –ø–∞–º—è—Ç–∏ LLM;
    * —Å–æ–∑–¥–∞—ë—Ç—Å—è Runnable —Å –ø–æ–¥–¥–µ—Ä–∂–∫–æ–π –∏—Å—Ç–æ—Ä–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏–π;
    * –æ—Ç—Ä–∏—Å–æ–≤—ã–≤–∞–µ—Ç—Å—è –∏—Å—Ç–æ—Ä–∏—è;
    * –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç—Å—è –Ω–æ–≤—ã–π –≤–≤–æ–¥ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    """
    # –ë–∞–∑–æ–≤–∞—è –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è —Å—Ç—Ä–∞–Ω–∏—Ü—ã Streamlit
    st.set_page_config(
        page_title="AI SOC-assistent",
        page_icon="üßä",
        initial_sidebar_state="expanded",
    )

    # ---------------- –°–ê–ô–î–ë–ê–†: –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ ----------------
    st.sidebar.title("–ù–∞—Å—Ç—Ä–æ–π–∫–∏")

    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º session_id –î–û –≤—ã–≤–æ–¥–∞ –Ω–∞—Å—Ç—Ä–æ–µ–∫, —á—Ç–æ–±—ã –º–æ–∂–Ω–æ –±—ã–ª–æ
    # –æ—Ç–æ–±—Ä–∞–∑–∏—Ç—å –µ–≥–æ –≤ —Å–∞–π–¥–±–∞—Ä–µ –∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –≤ –¥–∞–ª—å–Ω–µ–π—à–µ–º.
    session_id = init_session_id()

    # –°–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º–ø—Ç: –≤–ª–∏—è–µ—Ç –Ω–∞ —Å—Ç–∏–ª—å –∏ —Ñ–æ—Ä–º–∞—Ç –æ—Ç–≤–µ—Ç–æ–≤ –º–æ–¥–µ–ª–∏
    sys_prompt = st.sidebar.text_area(
        "–°–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º–ø—Ç",
        DEFAULT_SYSTEM_PROMPT,
    )

    # –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –æ—Ç–≤–µ—Ç–∞: —É–ø—Ä–∞–≤–ª—è–µ—Ç —Å—Ç–µ–ø–µ–Ω—å—é "–∫—Ä–µ–∞—Ç–∏–≤–Ω–æ—Å—Ç–∏" –º–æ–¥–µ–ª–∏
    temperature = st.sidebar.slider(
        "–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –æ—Ç–≤–µ—Ç–∞",
        min_value=0.0,
        max_value=1.0,
        value=0.2,
        step=0.1,
        help=(
            "–ß–µ–º –≤—ã—à–µ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞, —Ç–µ–º –±–æ–ª–µ–µ –≤–∞—Ä–∏–∞—Ç–∏–≤–Ω—ã–º–∏ –∏ –∫—Ä–µ–∞—Ç–∏–≤–Ω—ã–º–∏ "
            "–±—É–¥—É—Ç –æ—Ç–≤–µ—Ç—ã. –î–ª—è —Å—Ç—Ä–æ–≥–æ–≥–æ –¥–µ–ª–æ–≤–æ–≥–æ –∞—Å—Å–∏—Å—Ç–µ–Ω—Ç–∞ –ª—É—á—à–µ –¥–µ—Ä–∂–∞—Ç—å "
            "–∑–Ω–∞—á–µ–Ω–∏–µ –±–ª–∏–∂–µ –∫ 0."
        ),
    )

    # –û—Ç–æ–±—Ä–∞–∂–∞–µ–º —Ç–µ–∫—É—â–∏–π session_id –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏ –∏ –ø–æ–Ω–∏–º–∞–Ω–∏—è,
    # —á—Ç–æ –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ ¬´–ù–æ–≤—ã–π —á–∞—Ç¬ª –æ–Ω –º–µ–Ω—è–µ—Ç—Å—è.
    st.sidebar.text_input(
        "–¢–µ–∫—É—â–∏–π session_id",
        value=session_id,
        disabled=True,
    )

    # –ö–Ω–æ–ø–∫–∞ ¬´–ù–æ–≤—ã–π —á–∞—Ç¬ª: –æ—á–∏—â–∞–µ—Ç –∏—Å—Ç–æ—Ä–∏—é —Ç–µ–∫—É—â–µ–≥–æ –¥–∏–∞–ª–æ–≥–∞ –∏ –≥–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç
    # –Ω–æ–≤—ã–π session_id –¥–ª—è –ø–∞–º—è—Ç–∏ LLM.
    new_chat_clicked = st.sidebar.button(
        "–ù–æ–≤—ã–π —á–∞—Ç",
        help=(
            "–û—á–∏—Å—Ç–∏—Ç—å –∏—Å—Ç–æ—Ä–∏—é —Å–æ–æ–±—â–µ–Ω–∏–π –∏ –Ω–∞—á–∞—Ç—å –Ω–æ–≤—ã–π –Ω–µ–∑–∞–≤–∏—Å–∏–º—ã–π –¥–∏–∞–ª–æ–≥. "
            "–ü–æ–ª–µ–∑–Ω–æ, –∫–æ–≥–¥–∞ –≤—ã –ø–µ—Ä–µ—Ö–æ–¥–∏—Ç–µ –∫ –Ω–æ–≤–æ–π —Ç–µ–º–µ –∏ –Ω–µ —Ö–æ—Ç–∏—Ç–µ, —á—Ç–æ–±—ã "
            "–ø—Ä–æ—à–ª—ã–π –∫–æ–Ω—Ç–µ–∫—Å—Ç –≤–ª–∏—è–ª –Ω–∞ –æ—Ç–≤–µ—Ç—ã."
        ),
    )

    # ---------------- –ò–°–¢–û–†–ò–Ø –ß–ê–¢–ê (UI) ----------------
    # –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ–º, —á—Ç–æ history –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω–∞
    init_chat_history()

    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª ¬´–ù–æ–≤—ã–π —á–∞—Ç¬ª ‚Äî –æ—á–∏—â–∞–µ–º –≤–∏–∑—É–∞–ª—å–Ω—É—é –∏—Å—Ç–æ—Ä–∏—é
    # –∏ –≥–µ–Ω–µ—Ä–∏—Ä—É–µ–º –Ω–æ–≤—ã–π session_id –µ—â—ë –¥–æ –æ–±—Ä–∞—â–µ–Ω–∏—è –∫ LLM.
    if new_chat_clicked:
        reset_chat_history()
        session_id = reset_session_id()

    # ---------------- –°–û–ó–î–ê–ù–ò–ï RUNNABLE –° –ü–ê–ú–Ø–¢–¨–Æ ----------------
    # –ü–æ–ª—É—á–∞–µ–º API-–∫–ª—é—á –¥–ª—è OpenRouter –∏–∑ .env –∏–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è
    api_key = get_openrouter_api_key()

    # –°–æ–∑–¥–∞—ë–º RunnableWithMessageHistory:
    # –≤–Ω—É—Ç—Ä–∏ —Å–æ–∑–¥–∞—ë—Ç—Å—è/–±–µ—Ä—ë—Ç—Å—è –∏–∑ –∫–µ—à–∞ LLM –∏ prompt-—Ü–µ–ø–æ—á–∫–∞.
    chat_runnable = create_chat_runnable(
        system_prompt=sys_prompt,
        api_key=api_key,
        temperature=temperature,
    )

    # ---------------- –û–°–ù–û–í–ù–û–ô UI ----------------
    st.title("AI-–∞–≥–µ–Ω—Ç –ø–æ–¥–¥–µ—Ä–∂–∫–∏ L2 SOC")

    # –°–Ω–∞—á–∞–ª–∞ –≤—ã–≤–æ–¥–∏–º –∏—Å—Ç–æ—Ä–∏—é, —á—Ç–æ–±—ã —Å—Ç–∞—Ä—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –æ—Ç–æ–±—Ä–∞–∂–∞–ª–∏—Å—å –≤—ã—à–µ
    render_history()

    # –ü–æ–ª–µ –≤–≤–æ–¥–∞ –Ω–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ ¬´–Ω–∏–∂–Ω–µ–π¬ª —á–∞—Å—Ç–∏ —á–∞—Ç–∞
    user_input = st.chat_input("–í–≤–µ–¥–∏—Ç–µ –≤–∞—à –∑–∞–ø—Ä–æ—Å")

    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∏—á–µ–≥–æ –Ω–µ –≤–≤—ë–ª ‚Äî –ø—Ä–æ—Å—Ç–æ –∑–∞–≤–µ—Ä—à–∞–µ–º —Ä–µ–Ω–¥–µ—Ä
    if not user_input:
        return

    # –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤–æ–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –∏—Å—Ç–æ—Ä–∏—é –∏ –Ω–∞ —ç–∫—Ä–∞–Ω
    add_user_message(user_input)

    # –í—ã–∑—ã–≤–∞–µ–º –º–æ–¥–µ–ª—å —Å —É—á—ë—Ç–æ–º –ø–∞–º—è—Ç–∏ (RunnableWithMessageHistory).
    # –ü–µ—Ä–µ–¥–∞—ë–º session_id —á–µ—Ä–µ–∑ config, —á—Ç–æ–±—ã –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –¥–∏–∞–ª–æ–≥–∞
    # –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å–≤–æ—é –Ω–µ–∑–∞–≤–∏—Å–∏–º—É—é –∏—Å—Ç–æ—Ä–∏—é —Å–æ–æ–±—â–µ–Ω–∏–π.
    with st.spinner("–ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞—é –æ—Ç–≤–µ—Ç..."):
        try:
            result = chat_runnable.invoke(
                {"input": user_input},
                config={"configurable": {"session_id": session_id}},
            )
            answer = result.content
        except Exception as exc:
            # –í —Å–ª—É—á–∞–µ –æ—à–∏–±–∫–∏ –≤—ã–≤–æ–¥–∏–º –µ—ë –≤ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–µ –∏ –ª–æ–≥–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç
            answer = f"–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞: {exc}"
            st.error(answer)

    # –î–æ–±–∞–≤–ª—è–µ–º –æ—Ç–≤–µ—Ç –∞—Å—Å–∏—Å—Ç–µ–Ω—Ç–∞ –≤ –∏—Å—Ç–æ—Ä–∏—é –∏ –Ω–∞ —ç–∫—Ä–∞–Ω
    add_assistant_message(answer)


if __name__ == "__main__":
    main()
